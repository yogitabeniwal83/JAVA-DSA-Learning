package Methods;

public class BuiltInMethod {
    public static void main(String[] args) {
        // Math. wale
        System.out.println(Math.sqrt(64));
        System.out.println(Math.cbrt(100));
        System.out.println(Math.abs(-654));
        System.out.println(Math.floor(-7.8));  // floor -> greatest integer
        System.out.println(Math.ceil(3.999));
        System.out.println(Math.min(3.78,9));
        System.out.println(Math.max(2.4,2.41));
        System.out.println(Math.pow(2,6));
    }
}
