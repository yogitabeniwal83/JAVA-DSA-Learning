package basic;

public class ArithmeticOperationOnInt {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);


        double a = 5;
        double b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}
